package com.yearup;

public class CellPhone {
    String serialNumber;
    String model;
    String carrier;
    String phoneNumber;
    String owner;

    CellPhone(String serialNumber,String model,String carrier, String phoneNumber, String owner){
        this.serialNumber= serialNumber;
        this.model= model;
        this.carrier=carrier;
        this.phoneNumber=phoneNumber;
        this.owner=owner;
    }




}
